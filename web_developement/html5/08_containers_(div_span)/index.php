	Block Tags vs Inline Tags:
	Block tags take up the entire width of the page. It doesn't matter how much space there is next to 
	them, they will not allow any other tag to sit next to them on a page. E.g. are horizontal rule and paragraph tags.

	Inline elements only take up the space they need. They allows other tags to sit next to them on a page.
	E.g. are anchor, bold, italic tags etc.

	Spans vs. Divs
	These are two types of containers.
	Spans are inline containers
	Divs are block containers
	Both these containers require closing tags. i.e. <span></span> and <div></div>

	Anything you put inside a div is going to act like a block element
	Anything you put inside a span is going to act like a inline element.

