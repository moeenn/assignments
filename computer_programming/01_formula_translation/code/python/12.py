num1, num2 = input("Enter two numbers: ").split()
num1 = int(num1)
num2 = int(num2)

result = num1 + num2
print("%d + %d = %d" % (num1, num2, result))

result = num1 - num2
print("%d - %d = %d" % (num1, num2, result))

result = num1 * num2
print("%d * %d = %d" % (num1, num2, result))

result = num1 / num2
print("%d / %d = %.2f" % (num1, num2, result))

result = num1 / num2
print("%d / %d = %d" % (num1, num2, result))

result = num1 % num2
print("%d %% %d = %d" % (num1, num2, result))